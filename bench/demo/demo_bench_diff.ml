(* Differential test: demo bench cases tested against EverParse C stubs
   via Wire_diff. Cases and their C decode adapters are defined in
   demo_bench_cases.ml; the C stubs registry is generated by gen_stubs.ml.
   This module is fully generic -- adding a new projection case requires no
   changes here. *)

open Wire
open Demo_bench_cases

type case = { id : Demo_bench_cases.id; label : string; packed : Wire_diff.t }

let of_case (type a) (Read_case c : a read_case) =
  let stubs = C_stubs.stubs_of_name (Everparse.Raw.struct_name c.struct_) in
  let size = Codec.wire_size c.codec in
  let c_parse : bytes -> a =
    match c.of_c_field with
    | Of_int f -> fun buf -> f (stubs.projected_int buf 0)
    | Of_int64 f -> fun buf -> f (stubs.projected_int64 buf 0)
  in
  let read buf =
    let bytes = Bytes.of_string buf in
    if not (stubs.check bytes) then None else Some (c_parse bytes)
  in
  let ocaml_read buf =
    if String.length buf < size then None
    else Some (c.get (Bytes.of_string buf) 0)
  in
  let project v =
    let buf = Bytes.create size in
    Codec.encode c.codec v buf 0;
    c.get buf 0
  in
  let write _ = None in
  let packed =
    Wire_diff.harness ~name:c.label ~codec:c.codec ~read ~write ~project
      ~equal:c.equal ~ocaml_read ()
  in
  { id = c.id; label = c.label; packed }

let cases = List.map (fun (C rc) -> of_case rc) projection_cases

let verify_of_id id =
  let c = List.find (fun c -> c.id = id) cases in
  let (C (Read_case r)) =
    List.find (fun (C (Read_case r)) -> r.id = id) projection_cases
  in
  let stubs = C_stubs.stubs_of_name (Everparse.Raw.struct_name r.struct_) in
  fun () ->
    Array.iteri
      (fun i item ->
        let buf = Bytes.unsafe_to_string item in
        (* Check projected C path vs OCaml *)
        (match c.packed.test_read buf with
        | Wire_diff.Match -> ()
        | Wire_diff.Both_failed ->
            Fmt.failwith "%s: read failed on item %d" c.label i
        | Wire_diff.Value_mismatch msg ->
            Fmt.failwith "%s: read mismatch on item %d: %s" c.label i msg
        | Wire_diff.Only_c_ok msg ->
            Fmt.failwith "%s: only C succeeded on item %d: %s" c.label i msg
        | Wire_diff.Only_ocaml_ok msg ->
            Fmt.failwith "%s: only OCaml succeeded on item %d: %s" c.label i msg);
        (* Check FFI parse path doesn't crash *)
        stubs.ffi_parse item 0)
      r.dataset.items
